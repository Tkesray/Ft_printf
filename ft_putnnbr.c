#include "ft_printf.h"

void	ft_putnnbr(unsigned long long n)
{
	
	if (n > 9)
	{
		ft_putnnbr(n / 10);
		ft_putnnbr(n % 10);
	}
	else
		ft_putchar('0' + n);
}
